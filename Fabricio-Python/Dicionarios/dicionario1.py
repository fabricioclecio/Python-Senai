# Cria um dicionário usando as chaves.
frutas = {'banana': 'amarela', 'maça':'vermelha', 'uva':'roxa'}

# Cria um dicionário usando o método construtor(função embutida).
cachorros = dict(pinscher ="pequeno", rotweiller="médio", labrador="médio")

# Cria um dicionário vazio para entrada de dados do usuário, preenchendo o dicionário.
carros = {}

# Verifica se as estruturas de dados são dicionários.
print(type(frutas))
print(type(cachorros))
print(type(carros))
