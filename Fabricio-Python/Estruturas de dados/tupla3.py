 # Cria duas tupla numéricas.
tupla1 = (1, 2, 3)
tupla2 = (4, 5, 6)

# Concatena uma tupla.
concatenada = tupla1 + tupla2
print(f"Tuplas concatenadas: {concatenada}")

# Repete uma tupla.
repetida = tupla1 * 2
print(f"Tupla repetida: {repetida}")

# Verifica a ocorrência de um elemento na tupla.
contagem = repetida.count(1)
print(f"O número 1 aparece {contagem} vezes na tupla.")


